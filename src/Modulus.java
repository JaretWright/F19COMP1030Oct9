public class Modulus {
    public static void main(String[] args)
    {
        double num1 = 2;
        double num2 = 5;

        System.out.printf("5 / 2 = %d %n", 5/2);
        System.out.printf("5 / 2 = %.1f (using doubles)%n",num2/num1);

        System.out.printf("5 %% 2 = %d %n", 5%2);
        System.out.printf("4 %% 2 = %d %n", 4%2);
    }
}
